import { toast } from "react-toastify";

export const ToastifySuccess = (text, position) => {
  toast.success(text, {
    position: "bottom-center",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
};

